                        <p>
                            Televizier richt zich op de Nederlandstalige programma’s waar iedereen graag over meepraat: amusement, series, soaps en films. Televizier.nl gaat verder waar het blad stopt en geeft elke dag het laatste nieuws over de wereld van televisie en de sterren, met veel videomateriaal en een uitgebreid archief over de populairste programma’s. 
